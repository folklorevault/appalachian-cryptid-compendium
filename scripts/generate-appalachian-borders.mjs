import { readFileSync, writeFileSync } from "node:fs";
import { feature } from "topojson-client";
import { geoPath, geoEquirectangular } from "d3-geo";

const TOPOJSON_PATH = new URL(
  "../node_modules/us-atlas/states-10m.json",
  import.meta.url
);

const STATES = {
  "01": "AL",
  "13": "GA",
  "21": "KY",
  "37": "NC",
  "45": "SC",
  "47": "TN",
  "51": "VA",
  "54": "WV",
};

const VIEWBOX_WIDTH = 1000;
const VIEWBOX_HEIGHT = 620;

const topology = JSON.parse(readFileSync(TOPOJSON_PATH, "utf8"));
const allStates = feature(topology, topology.objects.states);

const appalachian = {
  type: "FeatureCollection",
  features: allStates.features.filter((f) => STATES[f.id]),
};

const projection = geoEquirectangular().fitSize(
  [VIEWBOX_WIDTH, VIEWBOX_HEIGHT],
  appalachian
);

const path = geoPath(projection);
const [scale, [tx, ty]] = [projection.scale(), projection.translate()];

const stateEntries = appalachian.features.map((f) => ({
  fips: f.id,
  code: STATES[f.id],
  d: path(f),
}));

const output = `// AUTO-GENERATED by scripts/generate-appalachian-borders.mjs
// Source: us-atlas states-10m.json (TopoJSON, CC0)
// Re-run: \`node scripts/generate-appalachian-borders.mjs\`

export const VIEWBOX_WIDTH = ${VIEWBOX_WIDTH};
export const VIEWBOX_HEIGHT = ${VIEWBOX_HEIGHT};

const PROJECTION_SCALE = ${scale};
const PROJECTION_TX = ${tx};
const PROJECTION_TY = ${ty};

export interface StateBorder {
  fips: string;
  code: string;
  d: string;
}

export const APPALACHIAN_STATES: readonly StateBorder[] = ${JSON.stringify(
  stateEntries,
  null,
  2
)} as const;

/**
 * Project [lng, lat] into the same SVG viewBox the borders use.
 * Replicates d3-geo's geoEquirectangular forward transform with the fitted
 * scale/translate baked at generation time, so we ship zero map runtime.
 */
export function projectCoordinate(lng: number, lat: number): [number, number] {
  const lambda = (lng * Math.PI) / 180;
  const phi = (lat * Math.PI) / 180;
  return [
    PROJECTION_SCALE * lambda + PROJECTION_TX,
    -PROJECTION_SCALE * phi + PROJECTION_TY,
  ];
}
`;

const OUT_PATH = new URL("../src/data/appalachian-borders.ts", import.meta.url);
writeFileSync(OUT_PATH, output);
console.log(`Wrote ${stateEntries.length} state borders to src/data/appalachian-borders.ts`);
console.log(`scale: ${scale}, translate: [${tx}, ${ty}]`);
